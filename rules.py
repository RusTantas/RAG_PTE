def find_by_keywords(remark: str) -> str:
    """Find ПТЭ/ИДП punkt by keywords in remark text."""
    remark_lower = remark.lower()
    for keywords, punkt in KEYWORD_TO_PUNKT.items():
        for kw in keywords:
            if kw in remark_lower:
                return punkt
    return None


def find_rule(remark: str) -> Rule:
    """Find Rule object by keywords in remark text."""
    remark_lower = remark.lower()
    for rule in ALL_RULES:
        for kw in rule.keywords:
            if kw in remark_lower:
                return rule
    return None


def find_rule_by_punkt(punkt_name: str) -> Rule:
    """Find Rule object by punkt name (e.g., 'Раздел 2 пункт 9 ПТЭ')."""
    punkt_normalized = punkt_name.lower().strip()
    for rule in ALL_RULES:
        if rule.punkt.lower() == punkt_normalized:
            return rule
    return None


def route_to_section(remark: str) -> str:
    """Route remark to section by keywords."""
    remark_lower = remark.lower()
    for keywords, section in SECTION_KEYWORDS.items():
        if any(kw in remark_lower for kw in keywords.split()):
            return section
    return None


def get_total_rule_count() -> int:
    """Get total number of rules in database."""
    return len(ALL_RULES)


def get_all_rules() -> tuple:
    """Get all rules as a tuple."""
    return ALL_RULES


def get_sections_summary() -> dict:
    """Get summary of all sections and rule counts."""
    summary = {}
    for section_id, section_data in SECTION_RULES.items():
        summary[section_id] = {
            "name": section_data["name"],
            "punkt_range": section_data["punkt_range"],
            "rule_count": len(section_data["rules"])
        }
    return summary