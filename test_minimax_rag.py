results = []
keyword_hits = 0
llm_hits = 0

for i, remark in enumerate(tqdm(test_remarks, desc="Обработка"), 1):
    # Сначала пробуем keyword matching
    keyword_result = find_by_keywords(remark)
    
    if keyword_result:
        predicted = keyword_result
        keyword_hits += 1
        method = "KEYWORD"
    else:
        # Используем LLM
        indices, _ = search_relevant_chunks(remark, chunks)
        context_chunks = [chunks[idx] for idx in indices[:10]] if indices else chunks[:5]
        
        raw_response = ask_minimax(remark, context_chunks)
        predicted = extract_punkt(raw_response)
        method = "LLM"
        llm_hits += 1
    
    correct = correct_answers.get(i, "НЕ УКАЗАН")
    
    # Сравниваем нормализованные ответы
    is_correct = normalize_answer(predicted) == normalize_answer(correct)
    
    results.append({
        "id": i,
        "remark": remark[:80],
        "predicted": predicted,
        "correct": correct,
        "is_correct": is_correct,
        "method": method
    })

# Статистика
