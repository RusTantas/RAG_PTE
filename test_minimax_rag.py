def find_by_keywords(remark):
    """
    Быстрый поиск по ключевым словам.
    Возвращает найденный пункт или None.
    """
    remark_lower = remark.lower()
    
    for keyword, punkt in KEYWORD_TO_PUNKT.items():
        if keyword in remark_lower:
            return punkt
    
    return None

