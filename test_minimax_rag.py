def split_by_punkt(text):
    """
    Разбивает текст по пунктам, сохраняя целостность каждого пункта.
    Пункты начинаются с "Раздел X пункт Y", "Приложение X пункт Y"
    """
    # Паттерны для начала пунктов
    punkt_patterns = [
        r'(Раздел\s+\d+\s+пункт\s+\d+)',
        r'(Приложение\s+\d+\s+пункт\s+\d+)',
        r'(ИДП\s+Раздел\s+\d+\s+пункт\s+\d+)',
    ]
    
    # Находим все начала пунктов
    positions = []
    for pattern in punkt_patterns:
        for match in re.finditer(pattern, text, re.IGNORECASE):
            positions.append((match.start(), match.group(1)))
    
    # Сортируем по позиции
    positions.sort(key=lambda x: x[0])
    
    # Если не нашли пунктов, возвращаем весь текст как один чанк
    if not positions:
        return [text]
    
    # Разбиваем по позициям
    chunks = []
    for i, (start, title) in enumerate(positions):
        end = positions[i + 1][0] if i + 1 < len(positions) else len(text)
        chunk = text[start:end].strip()
        if chunk:
            chunks.append(chunk)
    
    return chunks

