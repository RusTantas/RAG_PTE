def ask_minimax(remark, context_chunks):
    """
    Запрос к MINMAX модели.
    """
    # Собираем контекст
    context = "\n\n---\n\n".join(context_chunks[:5])
    
    user_prompt = f"""Ситуация (замечание):
{ remark }

Выдержки из документов:
{ context[:3000] }

Какой пункт правил нарушен? Ответь ТОЛЬКО номером пункта в формате:
- Раздел 2 пункт 9 ПТЭ
- Приложение 14 пункт 15 ИДП
Если точно не уверен, напиши "НЕ ОПРЕДЕЛЕНО"

Ответ:"""

    try:
        response = requests.post(
            MINMAX_URL,
            json={
                "model": "minimax",
                "messages": [
                    {"role": "system", "content": SYSTEM_PROMPT},
                    {"role": "user", "content": user_prompt}
                ],
                "temperature": 0.1,
                "max_tokens": 100
            },
            timeout=60
        )
        
        if response.status_code == 200:
            result = response.json()
            return result["choices"][0]["message"]["content"].strip()
        else:
            return f"ОШИБКА: {response.status_code} - {response.text}"
    except Exception as e:
        return f"ИСКЛЮЧЕНИЕ: {str(e)}"

def normalize_answer(text):
    """
    Нормализация ответа для сравнения.
    """
    text = text.lower().strip()
    text = re.sub(r'[№#]', '', text)
    text = re.sub(r'приложения?\s*(\d+)', r'приложение \1', text)
    text = re.sub(r'раздел\s+(\d+)', r'раздел \1', text)
    text = re.sub(r'пункт\s+(\d+)', r'пункт \1', text)
    text = re.sub(r'п\.', 'пункт', text)
    text = re.sub(r'\s+', ' ', text)
    return text.strip()

def extract_punkt(text):
    """
    Извлекает номер пункта из ответа модели.
    """
    # Паттерны для поиска
    patterns = [
        r'(раздел\s+\d+\s+пункт\s+\d+\s+птэ)',
        r'(приложение\s+\d+\s+пункт\s+\d+\s+идп)',
        r'(раздел\s+\d+\s+пункт\s+\d+)',
        r'(приложение\s+\d+\s+пункт\s+\d+)',
    ]
    
    for pattern in patterns:
        match = re.search(pattern, text, re.IGNORECASE)
        if match:
            return match.group(1).upper()
    
    if "НЕ ОПРЕДЕЛЕНО" in text.upper():
        return "НЕ ОПРЕДЕЛЕНО"
    
    return text

