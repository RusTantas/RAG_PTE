def search_relevant_chunks(remark, chunks, top_k=10):
    """
    Поиск релевантных чанков по замечанию.
    """
    # Определяем целевой раздел
    target_section = route_to_section(remark)
    
    # Сначала пробуем keyword matching
    keyword_result = find_by_keywords(remark)
    if keyword_result:
        # Ищем чанк с этим пунктом
        for i, chunk in enumerate(chunks):
            if keyword_result.replace(" ПТЭ", "").replace(" ИДП", "") in chunk:
                return [i], keyword_result
    
    # Если keyword matching не сработал, используем BM25-like поиск
    words = remark.lower().split()
    scores = []
    
    for i, chunk in enumerate(chunks):
        chunk_lower = chunk.lower()
        score = sum(1 for w in words if w in chunk_lower)
        
        # Бонус за совпадение с целевым разделом
        if target_section and target_section.lower().replace(" ", "") in chunk_lower.replace(" ", ""):
            score += 5
        
        # Бонус за совпадение номера пункта из keyword_map
        for kw, punkt in KEYWORD_TO_PUNKT.items():
            if kw in remark.lower() and punkt.replace(" ПТЭ", "").replace(" ИДП", "") in chunk:
                score += 10
        
        scores.append((i, score))
    
    # Сортируем по убыванию скора
    scores.sort(key=lambda x: x[1], reverse=True)
    
    if scores and scores[0][1] > 0:
        top_indices = [idx for idx, score in scores[:top_k]]
        return top_indices, None
    
    return [], None

